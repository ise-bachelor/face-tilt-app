// 課題文IDで課題文を取得
export const getPassageById = (id: string): Passage | undefined => {
  return passages.find(p => p.id === id);
};

// ランダムに課題文を選択（participant_idに基づく）
export const getPassageForParticipant = (participantId: string): Passage => {
  // participant_idをハッシュして課題文を選択
  const hash = participantId.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
  const index = hash % passages.length;
  return passages[index];
};
